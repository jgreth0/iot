
#ifndef _PRESENCE_ICMP_H_
#define _PRESENCE_ICMP_H_

#include "presence.hpp"

////////////////////////////////////////////////////////////////////////////////
// this module detects the presence of a device on the network using Internet
// Control Message Protocol (ICMP) Echo requests and Responses (ping).
////////////////////////////////////////////////////////////////////////////////
class presence_icmp : public presence {
private:
    ////////////////////////////////////////////////////////////////////////////
    // Configuration - only written by the constructor.
    ////////////////////////////////////////////////////////////////////////////
    char addr[64];

    ////////////////////////////////////////////////////////////////////////////
    // IO context - A single connection is used multiple times.
    ////////////////////////////////////////////////////////////////////////////
    int seq_num = 1;
    int sock = -1;

protected:
    ////////////////////////////////////////////////////////////////////////////
    //
    ////////////////////////////////////////////////////////////////////////////
    void sync(bool last = false);

public:
    ////////////////////////////////////////////////////////////////////////////
    //
    ////////////////////////////////////////////////////////////////////////////
    presence_icmp(char* name, char* addr, int time_limit = 300);
    presence_icmp(const char* name, const char* addr, int time_limit = 300);
};

#endif
